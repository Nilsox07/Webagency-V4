          <h2 id="mehrseitig">Wann brauchen Sie mehrere Seiten?</h2>
          <p>Als Faustregel: <strong>pro Suchbegriff, der Ihnen Kunden bringt, eine eigene Seite.</strong> Mehrseitig lohnt sich daher, wenn mindestens einer dieser Punkte zutrifft:</p>
          <ul>
            <li>Sie haben <strong>mehrere Leistungen</strong>, nach denen Kunden getrennt suchen (z.&nbsp;B. „Badsanierung“ und „Heizungsinstallation“).</li>
            <li>Sie bedienen <strong>mehrere Zielgruppen</strong> mit unterschiedlichen Bedürfnissen (Privat- und Geschäftskunden).</li>
            <li>Sie möchten mit <strong>Ratgeber-Inhalten</strong> Vertrauen und Sichtbarkeit aufbauen (jeder Artikel ist eine eigene Seite).</li>
            <li>Sie brauchen eigene Seiten für <strong>Team, Karriere, Referenzen</strong> oder mehrere Standorte.</li>
          </ul>
          <p>Wie OnPage-SEO auf jeder einzelnen Unterseite funktioniert (Title, Meta, Fokus-Keyword), lesen Sie auf der Seite <a href="leistung-webdesign.php">Website erstellen lassen zum Festpreis</a>.</p>

          <h2 id="kosten">Was kostet der Unterschied?</h2>
          <p>Bei Sartu sind die Schwellen klar definiert — alles Festpreise:</p>
          <ul>
            <li><strong>One-Pager (Start): 1.290&nbsp;€</strong> — 1 Seite mit bis zu 6 Sektionen, 1 Korrekturrunde.</li>
            <li><strong>Bis 8 Seiten (Wachstum): 3.290&nbsp;€</strong> — inkl. OnPage-SEO für jede Seite, 2 Korrekturrunden.</li>
            <li><strong>Bis 20 Seiten (Platzhirsch): 6.490&nbsp;€</strong> — inkl. Local-SEO-Setup, 3 Korrekturrunden.</li>
            <li><strong>Zusätzliche Seite</strong> über dem Paket-Kontingent: <strong>199&nbsp;€</strong>.</li>

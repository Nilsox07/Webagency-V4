          <h2 id="was-ist">Was ist ein One-Pager?</h2>
          <p>Ein One-Pager (auch Onepager oder One-Page-Website) ist eine Website, die aus <strong>einer einzigen, länger scrollbaren Seite</strong> besteht. Alle Kerninhalte — Angebot, Über-mich, Referenzen, Kontakt — folgen als Sektionen untereinander. Die Navigation springt zu den Abschnitten, statt auf Unterseiten zu wechseln.</p>
          <p>Beim Sartu-Onepager (Start-Paket, 1.290&nbsp;€) sind bis zu 6 solcher Inhaltssektionen enthalten, dazu Mobil-Optimierung, ein Kontaktformular, Basis-SEO und das DSGVO-Setup — fertig in 7 Werktagen nach Inhaltslieferung.</p>

          <h2 id="vorteile">Vorteile &amp; Grenzen des One-Pagers</h2>
          <p><strong>Die Vorteile:</strong></p>
          <ul>
            <li><strong>Schnell online:</strong> weniger Seiten = weniger Inhalte, die Sie liefern müssen, und kürzere Umsetzungszeit.</li>
            <li><strong>Günstiger:</strong> der niedrigste Einstiegspreis für eine professionelle Website.</li>
            <li><strong>Klarer Fokus:</strong> Besucher werden ohne Umwege zu Ihrem Angebot und zur Kontaktaufnahme geführt — ideal, wenn Anfragen das Ziel sind.</li>
            <li><strong>Wenig Pflege:</strong> eine Seite aktuell zu halten ist schneller als zehn.</li>
          </ul>
          <p><strong>Die ehrliche Grenze — SEO:</strong> Eine URL kann bei Google in der Regel nur für <strong>ein Hauptthema</strong> stark ranken. Ein One-Pager hat genau eine URL.</p>
          <div class="decision-grid two" style="margin:18px 0 22px;">

              <h3 id="fehler">Der häufigste (und teuerste) Fehler: Auftrag vor Antrag</h3>
              <p>Sobald Sie verbindlich beauftragen — mündlich, per Vertrag oder durch eine bezahlte Anzahlung —, gilt das Vorhaben oft als begonnen. Eine danach beantragte Förderung wird dann in der Regel abgelehnt. Reihenfolge deshalb immer: <strong>erst prüfen, dann beantragen, dann (nach Bestätigung) beauftragen</strong>.</p>

          <h2 id="was-ist">Was ist das BFSG?</h2>
          <p>Das <strong>Barrierefreiheitsstärkungsgesetz (BFSG)</strong> ist die deutsche Umsetzung des European Accessibility Act (EAA), einer EU-Richtlinie. Es gilt seit dem <strong>28.&nbsp;Juni&nbsp;2025</strong> und verpflichtet bestimmte Unternehmen, ihre Produkte und digitalen Dienstleistungen so zu gestalten, dass auch Menschen mit Behinderungen sie ohne fremde Hilfe nutzen können.</p>
          <p>Neu daran: Erstmals trifft eine Barrierefreiheits-Pflicht in größerem Umfang die <strong>Privatwirtschaft</strong> — vorher galten solche Pflichten in Deutschland im Wesentlichen für öffentliche Stellen. Für Websites heißt das: Bestimmte Angebote im Netz müssen barrierefrei bedienbar sein.</p>

          <h2 id="betroffen">Wer ist betroffen — und wer ausgenommen?</h2>
          <p><strong>Betroffen</strong> sind Unternehmen, die unter das Gesetz fallende Produkte oder Dienstleistungen <strong>für Verbraucher</strong> anbieten. Im Website-Kontext ist der wichtigste Fall der „elektronische Geschäftsverkehr“: Websites und Apps, über die Verbraucher <strong>digital Verträge abschließen</strong> können — also z.&nbsp;B.:</p>
          <ul>
            <li>Onlineshops mit Bestell- und Bezahlfunktion,</li>
            <li>Buchungs- und Reservierungssysteme (Termine, Tische, Zimmer),</li>
            <li>Portale mit kostenpflichtiger Registrierung oder Vertragsabschluss.</li>
          </ul>
          <p><strong>Ausgenommen</strong> sind <strong>Kleinstunternehmen, die Dienstleistungen anbieten</strong>: weniger als 10 Beschäftigte <strong>und</strong> höchstens 2&nbsp;Mio.&nbsp;€ Jahresumsatz oder Jahresbilanzsumme. Für viele Selbstständige, Handwerksbetriebe und kleine Dienstleister entfällt die Pflicht damit — die Ausnahme gilt allerdings für <em>Dienstleistungen</em>; wer als Kleinstunternehmen physische <em>Produkte</em> in den Verkehr bringt, sollte den Einzelfall prüfen lassen.</p>
          <p>Reine <strong>B2B-Angebote</strong> (nur an Geschäftskunden) fallen ebenfalls nicht unter das BFSG — es schützt Verbraucher.</p>

          <h2 id="normale-website">Gilt das für meine normale Firmen-Website?</h2>
          <p>In den meisten Fällen: <strong>nein</strong>. Eine klassische Unternehmenswebsite, die Ihr Angebot vorstellt, Referenzen zeigt und ein Kontaktformular hat, ist <strong>keine</strong> Dienstleistung im elektronischen Geschäftsverkehr — über sie wird kein Vertrag digital abgeschlossen. Solche reinen Informations-Websites sind vom BFSG in der Regel nicht erfasst.</p>
          <p>Aufpassen müssen Sie, sobald Ihre Website <strong>Verkaufs- oder Buchungsfunktionen</strong> bekommt: Ein Shop-Modul oder eine Online-Terminbuchung mit verbindlichem Abschluss kann die Einordnung ändern — dann zählt wieder die Frage, ob Sie unter die Kleinstunternehmen-Ausnahme fallen. Wenn Sie so eine Funktion planen (etwa beim <a href="leistung-webdesign.php">Erstellen Ihrer Website</a>), lohnt es sich, Barrierefreiheit von Anfang an mitzudenken.</p>

          <h2 id="konkret">Was bedeutet „barrierefrei“ konkret?</h2>
          <p>Der praktische Maßstab sind die <strong>WCAG</strong> (Web Content Accessibility Guidelines) — die international etablierten Richtlinien für barrierefreie Web-Inhalte. Die wichtigsten Grundlagen:</p>
          <ul>
            <li><strong>Kontraste:</strong> Text muss sich ausreichend vom Hintergrund abheben (z.&nbsp;B. kein hellgrauer Text auf Weiß).</li>
            <li><strong>Alt-Texte:</strong> Bilder brauchen Alternativtexte, damit Screenreader sie beschreiben können.</li>
            <li><strong>Tastaturbedienung:</strong> Alle Funktionen — Menüs, Formulare, Buttons — müssen ohne Maus erreichbar sein, mit sichtbarem Fokus.</li>
            <li><strong>Verständliche Sprache:</strong> klare Struktur, kurze Sätze, eindeutige Beschriftungen statt Fachjargon.</li>
            <li><strong>Saubere Semantik:</strong> korrekte Überschriften-Hierarchie und HTML-Struktur, damit assistive Technik die Seite „versteht“.</li>
            <li><strong>Formulare:</strong> beschriftete Felder und Fehlermeldungen, die erklären, was zu korrigieren ist.</li>
          </ul>
          <p>Was viele dieser Begriffe im Detail bedeuten, erklärt unser <a href="ratgeber.php">Webdesign-Lexikon</a> — u.&nbsp;a. die Einträge zu Barrierefreiheit und BFSG.</p>

          <h2 id="verstoesse">Was passiert bei Verstößen?</h2>
          <p>Die Einhaltung überwachen die <strong>Marktüberwachungsbehörden</strong> der Länder. Sie können Nachbesserung anordnen und im Ernstfall die Bereitstellung der Dienstleistung einschränken; außerdem sieht das Gesetz <strong>Bußgelder</strong> vor. Daneben besteht das praktische Risiko von <strong>Abmahnungen</strong> durch qualifizierte Verbände.</p>
          <p>Wie hoch das Risiko im Einzelfall ist, hängt von Ihrem Angebot, Ihrer Unternehmensgröße und der konkreten Umsetzung ab — das kann und darf Ihnen nur eine anwaltliche Beratung verbindlich beantworten.</p>

          <h2 id="freiwillig">Lohnt sich Barrierefreiheit auch freiwillig?</h2>
          <p><strong>Ja — aus zwei handfesten Gründen.</strong> Erstens erreichen Sie mehr Menschen: Nutzer mit Sehschwäche, motorischen Einschränkungen oder schlicht ältere Besucher profitieren von Kontrast, Tastaturbedienung und klarer Sprache — und werden eher zu Kunden, wenn Ihre Seite sie nicht aussperrt.</p>
          <p>Zweitens hilft Barrierefreiheit der <strong>Sichtbarkeit</strong>: Die saubere Semantik, die Screenreader brauchen (Überschriften-Struktur, Alt-Texte, beschriftete Elemente), ist genau das, was auch Suchmaschinen und KI-Systeme zum Verstehen einer Seite nutzen. Barrierefreiheit und SEO ziehen am selben Strang — ein Grund, warum Sartu Kontraste, Alt-Texte, Tastatur-Fokus und semantische Struktur bei jeder <a href="leistung-webdesign.php">Website-Erstellung</a> standardmäßig berücksichtigt.</p>


    // Kontaktformular -> vorbereitete E-Mail öffnen (kein Backend nötig)
    (function () {
      var form = document.getElementById('contactForm');
      if (!form) return;
      var status = document.getElementById('contactFormStatus');
      function showStatus(message) {
        if (!status) return;
        status.textContent = message;
        status.hidden = false;
      }
      form.addEventListener('submit', function (e) {
        e.preventDefault();
        var v = function (id) { var el = document.getElementById(id); return el ? el.value.trim() : ''; };
        var name = v('cf-name'), email = v('cf-email');
        if (!name || !email) {
          showStatus('Bitte geben Sie mindestens Ihren Namen und Ihre E-Mail an.');
          if (!name && document.getElementById('cf-name')) document.getElementById('cf-name').focus();
          else if (!email && document.getElementById('cf-email')) document.getElementById('cf-email').focus();
          return;
        }
        if (status) status.hidden = true;
        var body =
          'Name: ' + name + '\n' +
          'E-Mail: ' + email + '\n' +
          'Website: ' + (v('cf-website') || '–') + '\n' +
          'Interesse: ' + v('cf-paket') + '\n\n' +
          'Nachricht:\n' + (v('cf-msg') || '–');
        var subject = 'Kostenloser Website-Check – ' + name;
        window.location.href = 'mailto:hallo@sartu.de?subject=' +
          encodeURIComponent(subject) + '&body=' + encodeURIComponent(body);
      });
    })();
